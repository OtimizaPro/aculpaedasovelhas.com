                <h1 class="section-title">Minha Trajetória — Anderson Belém Costa</h1>
                
                <h3>Infância e primeiros passos na fé</h3>
                <p>Nasci em 11 de outubro de 1981, no Rio de Janeiro. Fui criado dentro da fé católica, fiz o catecismo e a primeira comunhão. Durante a crisma, porém, algo em mim se rompeu. Eu me distanciei da Igreja Católica Apostólica Romana e iniciei uma caminhada de busca — uma jornada espiritual e intelectual que me levaria por muitos caminhos antes de voltar a Jesus Cristo.</p>
                <p>Mesmo afastado, conservei o respeito pelas tradições da minha infância. Batizei meus dois primeiros filhos, Jéssica e Nicolas Belém, e me casei no dia do meu aniversário de 22 anos, em 11 de outubro de 2003, na Paróquia São Roque de Vila Valqueire, na Zona Oeste do Rio.</p>

                <h3>Juventude e primeiras decepções religiosas</h3>
                <p>Na adolescência, frequentei uma igreja batista em Honório Gurgel, subúrbio do Rio de Janeiro. Buscava respostas e comunhão, mas me decepcionei com a ênfase exagerada em dízimos e com o que percebia como imitação de manifestações pentecostais — muita gritaria, supostas línguas angelicais sem entendimento e canções cheias de citações bíblicas mal compreendidas. Eu via emoção, mas pouca verdade espiritual.</p>

                <h3>Vida adulta e nova busca</h3>
                <p>Na vida adulta, minha esposa se batizou em uma igreja neopentecostal de parede preta, dessas voltadas à prosperidade. Apresentamos nossa filha caçula, Antonella Belém, ali. Contudo, novamente me desiludi. Vi mais teatro do que ministério — um “show” religioso em vez de pregação do arrependimento. Mesmo assim, registrei em mim alguns versículos mencionados pelo pastor, que na prática agia mais como ator de stand-up do que como servo de Deus.</p>

                <h3>O milagre da Páscoa</h3>
                <p>Durante a pandemia de COVID-19, minha filha Antonella foi acometida por uma doença autoimune sem diagnóstico inicial. Seu quadro piorava a cada dia. Ela emagreceu, não conseguia mais colocar os pés no chão e ficou abatida. Na Semana Santa, chegamos ao limite do desespero.</p>
                <p>Mas no Domingo da Ressurreição, algo mudou. Finalmente veio o diagnóstico, o tratamento médico correto começou, e naquele mesmo domingo ela comeu chocolate — com o olhar vivo, sem olheiras, o rosto renovado. Na quarta-feira, já estava em casa, brincando. Foi um milagre.</p>
                <p>Entendi naquele dia que Cristo havia me chamado de volta. Era como se Ele tivesse dito: “Estou aqui, como sempre estive.”</p>

                <h3>Retorno a Cristo</h3>
                <p>Aquele episódio me fez olhar para Jesus novamente após três décadas mergulhado em filosofia, gnosticismo, misticismo, espiritualismo, cabala, budismo e hermetismo. Tudo o que eu buscava estava diante de mim o tempo todo: a verdade viva da Palavra de Deus.</p>
                <p>Nos anos seguintes, enfrentei depressão profunda e recebi diagnóstico de TDAH e síndromes tardias. Depois de 15 anos na Polícia do Estado do Rio de Janeiro, me afastei do serviço para me dedicar ao empreendedorismo tecnológico. Foram anos de reestruturação interior e espiritual.</p>
                <p>Em meio a isso, Jesus Cristo começou a me revelar um novo entendimento das Escrituras — não com fatos novos sobre o futuro, mas com a leitura correta do que sempre esteve escrito.</p>

                <h3>A transformação de “A Culpa é das Ovelhas”</h3>
                <p>Eu já escrevia o livro “A Culpa é das Ovelhas”, originalmente concebido como uma crítica sociológica sobre sistemas de poder, manipulação e comunicação de massa. Inspirado por ideias de Carlos Nepomuceno, cuja palestra assisti em 2014, mergulhei no estudo da comunicação como estrutura civilizacional. Fui aluno por seis anos da Escola Bimodal, onde aprendi que a descentralização da comunicação humana é essencial para a liberdade.</p>
                <p>Contudo, com o tempo e com as revelações recebidas, compreendi algo maior: a descentralização humana não é a solução. A verdadeira libertação está na centralização em Jesus Cristo — o único centro legítimo, o único capaz de sustentar a verdade, a justiça e a vida.</p>
                <p>Assim, “A Culpa é das Ovelhas” deixou de ser apenas uma análise dos sistemas humanos para se tornar uma obra espiritual, onde Cristo é o foco. O capítulo que antes tratava da Bíblia e das ovelhas se tornou o coração de todo o livro, e também o centro da minha própria vida.</p>

                <h3>Batismo no Espírito Santo e revelações</h3>
                <p>Em dado momento, percebi que um louvor antigo ecoava na minha mente havia décadas: a canção “João Viu”. Coloquei-a para tocar, e naquele instante fui batizado pelo Espírito Santo. Caí em choro incontrolável, sem entender o motivo, até perceber que o Espírito me fazia reconhecer que eu vinha recebendo revelações diretas do livro da Revelação de Jesus Cristo, chamado erroneamente de Apocalipse.</p>
                <p>A partir desse dia, recebi revelações sobre a data do retorno de Jesus, conforme as profecias de Daniel. Compreendi o número da besta, os nomes das bestas da terra e do mar, identifiquei o primeiro anticristo, o falso profeta, as duas testemunhas, o dragão, a mulher prostituta, a grande Babilônia, o cavaleiro branco e as bestas de Daniel.</p>
                <p>Desde então, continuo recebendo revelações diárias sobre a interpretação correta da Bíblia, sempre confirmadas pelo próprio texto sagrado.</p>

                <h3>Propósito e missão</h3>
                <p>Minha base é exclusivamente a Escritura, e dela retiro tudo o que ensino e escrevo.</p>
                <p>Uno o que aprendi na faculdade de Literatura Brasileira, minha paixão por texto, palavra, comunicação e poesia, com o Texto Sagrado, para construir uma nova visão — uma leitura que revela o homem da iniquidade e expõe o grande engano amplamente disseminado na internet e no meio escatológico.</p>
                <p>Essa é a minha missão: usar o dom da Comunicação, da Palavra e da Revelação para apontar, testemunhar sobre as coisas de Deus, as coisas de Jesus Cristo de Nazaré, o Deus que nasceu em Belém, o centro de toda a verdade e o início de toda a vida.</p>
